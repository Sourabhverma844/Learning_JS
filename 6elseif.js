// 90 to 100 = A Grade
// 70 to 89 = B Grade
// 50 to 69 = C Grade
// lower than 50 = Fail
let Score = 64.9
if(Score >= 90){
    console.log('A Grade')
}
else if(Score>=70 && Score<=89){
    console.log('B Grade')
}
else if(Score>=50 && Score<=69){
    console.log('C Grade')
}
else{
    console.log('Fail')
}

console.log(Score);